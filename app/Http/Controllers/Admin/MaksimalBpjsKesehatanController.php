     * @return \Illuminate\Http\Response
     */
    public function update(MaksimalBpjsKesehatanRequest $request, $id)
    {
        //
        if (auth()->user()->roles != 'ADMIN' && auth()->user()->roles != 'HRD') {
            abort(403);
        }
        $data = $request->all();
